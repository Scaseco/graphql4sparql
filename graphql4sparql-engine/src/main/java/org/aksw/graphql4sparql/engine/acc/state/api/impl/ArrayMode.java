package org.aksw.graphql4sparql.engine.acc.state.api.impl;

/**
 * Array mode configuration for aggregator states.
 */
public enum ArrayMode {
    /**
     * Array mode is disabled.
     */
    OFF,
    /**
     * Array mode is flat.
     */
    FLAT,
    /**
     * Array mode is nested.
     */
    NESTED
}
